package turkcedersler;

public class mathsinifi {
    public static void main(String[] args) {
        /*
        // Math sinifi metotlar
// Math.ceil(x) => bir büyük tam sayiya yuvarlama yapar.
// Math.cos (x) => cos degerini döner
// Math.sin(x) => sin degerini döner
// Math.floor (x) => kücük tam sayiya yuvarlar
// Math.max(x,y) => büyük sayiy verir
// Math.min(x,y) => kücük sayy1 verir
math.pow(3,5) => 3^5 üs alir
// Math.random() => 1 ve 0 arasinda rastegele deger
// Math. round(X) = yakin odan tam sayiya doner
math.abs(x) = mutlak deger
         */
        System.out.println(Math.ceil(1.1));
        System.out.println(Math.cos(30));
        System.out.println(Math.floor(2.1));
        System.out.println(Math.max(1.2, 1));
        System.out.println(Math.pow(3,2));
        System.out.println(Math.round(8.7));
        System.out.println(Math.abs(-11));
        System.out.println(Math.sqrt(16));

    }
}
