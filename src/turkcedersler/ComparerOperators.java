package turkcedersler;

public class ComparerOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        boolean result = a == b;
        boolean result2 = a != b;
        boolean result3 = a > b;



        System.out.println(result);
        System.out.println(result2);
        System.out.println(result3);
    }
}
